<docs>The `fd-calendar` component is usually displayed inside an `fd-popover`. Scroll down to see an example if you are unsure how to do that. Even though the calendar is a very complex component getting started is as easy as pie. This

```html
<fd-calendar />
```

gets you the default calendar displayed below. `fd-calendar` has a lot of customization points. Please refer to the examples below for details.
</docs>
<tip>`fd-calendar` is still under heavy development. Things might break.</tip>
